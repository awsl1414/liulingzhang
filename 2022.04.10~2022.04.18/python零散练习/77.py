#面对对象（oop）(之前学的都是面向过程)

#开发复杂项目，用面向过程，没有固定套路，开发难度很大

#面向对象特点：
#1.注重对象和职责
#2.是专门应对复杂项目开发，提供固定套路
#3.需要在面向过程的基础上，再学习一些面向对象的语法

#核心概念：
#1.类：负责创建对象（图纸）
    #特征被称为属性
    #行为被成为行为
#2.对象（图制造出的物品）
#注意：1.先有类，后有对象2.类有一个，对象有多个
#类起名应注意满足大驼峰法
    #大驼峰法：每一个单词的首字母大写
    #单词与单词之间没有下划线
#类的确定：名词提取法
    #分析整个业务流程，出现的名词，通常就是找到的类
#类的属性和方法的确定方法
    #对对象的特征描述，通常可以定义为属性
    #对象具有的行为，通常定义为方法